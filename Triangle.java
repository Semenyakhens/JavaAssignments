class Triangle{   
	public static void main(String args[]){  
	
	int n = 4;   
	//loop for rows  
	for(int i=0; i < n; i++){   
		   
	//loop for columns  
	for(int j=0; j<=i; j++)  
	{   
	//prints rows and columns  
	System.out.print(i + j);   
	  
	   
}  
	System.out.println();
}  
}
}